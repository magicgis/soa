package com.yeepay.g3.facade.soa.center.dto;

import java.io.Serializable;

/**
 * @author by menghao.chen
 */
public class ServerInfoDTO implements Serializable {
	private static final long serialVersionUID = -8720137658699555983L;

	private String address;
	private Integer port;
	private Integer clientSize;

	public String getAddress() {
		return address;
	}

	public void setAddress(String address) {
		this.address = address;
	}

	public Integer getPort() {
		return port;
	}

	public void setPort(Integer port) {
		this.port = port;
	}

	public Integer getClientSize() {
		return clientSize;
	}

	public void setClientSize(Integer clientSize) {
		this.clientSize = clientSize;
	}
}
