public enum AuthStatusEnum {
	/**
	 * 同步初始时的临时状态
	 */
	UNSYNC("待同步"),

	PUBLIC("公开授权"),

	AUTHED("已授权"),

	FORBID("未授权");

	private String desc;

	private AuthStatusEnum(String desc) {
		this.desc = desc;
	}

	public String getDesc() {
		return desc;
	}

	public void setDesc(String desc) {
		this.desc = desc;
	}
}
